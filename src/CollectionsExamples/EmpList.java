package CollectionsExamples;

public class EmpList {
    String string;
    int num;

    public EmpList(String string, int num) {
        this.string = string;
        this.num = num;
    }

    public String getString() {
        return string;
    }

    public void setString(String string) {
        this.string = string;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }
}
